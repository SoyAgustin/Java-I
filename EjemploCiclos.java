
public class EjemploCiclos {
	public static void main(String[] args) {
		System.out.println("Con ciclo For");
		for(int i=0;i<11;i++) {
			System.out.println(i);
		}
		
		System.out.println("\n");
		System.out.println("Con ciclo while");
		int i=0;
		while(i<11){
			System.out.println(i);
			i++;
		}
	}
}
